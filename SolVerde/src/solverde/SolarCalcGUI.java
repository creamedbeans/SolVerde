    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        // TODO add your handling code here:
    calculateButton.addActionListener(event-> {
      try {
        //inputs from the GUI
        double roofSize = Double.parseDouble(RoofDimensionInput.getText());
        int panelCount = Integer.parseInt(noOfPanels.getSelectedItem().toString());
        String location = LocationSelector.getSelectedItem().toString(); // Provinces
        String roofOrientation = RoofOrientation.getSelectedItem().toString(); // Directions

        //instances of RoofDetails and SolarPanel
        RoofDetails roofDetails = new RoofDetails(roofSize, roofOrientation, 50, location); // Sunlight exposure fixed at 100% for simplicity
        SolarPanel solarPanel = new SolarPanel(panelCount, 18.0); // Efficiency: 18%

        //calculations using SolarCalculator
        SolarCalculator calculator = new SolarCalculator(roofDetails, solarPanel);
        double energyProduction = calculator.calculateEnergyProduction(); // energy in kWh/year
        double co2Reduction = calculator.calculateCO2Reduction(); // CO2 reduction in kg/year
        double costSavings = calculator.calculateCostSavings(0.12); // electricity rate â‚¬0.12/kWh

        //gui outputs
        EnergyProductionOutput.setText(String.format("%.2f kWh/year", energyProduction));
        CO2Output.setText(String.format("%.2f kg CO2/year", co2Reduction));
        CostSavingOutput.setText(String.format("â‚¬%.2f", costSavings));

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Please fill in the required fields", "Input Error", JOptionPane.ERROR_MESSAGE);
    }
});







    }//GEN-LAST:event_calculateButtonActionPerformed

