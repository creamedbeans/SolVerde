    private void SaveCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveCalcActionPerformed
           // takes user info from 3 fiedlds
    String input1 = EnergyProductionOutput.getText(); // Eenergy production
    String input2 = CO2Output.getText();             // CO2 output
    String input3 = CostSavingOutput.getText();      // cost savings

    // make sure none of outputs are empty
    if (input1.isEmpty() || input2.isEmpty() || input3.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields!");
        return;
    
    }//GEN-LAST:event_SaveCalcActionPerformed

         // format calcs
    String calculation = "Energy Production: " + input1 + ", CO2 Output: " + input2 + ", Cost Savings: " + input3;
        saveCalculation(calculation); // Save the user inputs
        
        // add string to arraylist
        results.add(calculation);
        JOptionPane.showMessageDialog(this, "Calculation saved!");
        
    }
