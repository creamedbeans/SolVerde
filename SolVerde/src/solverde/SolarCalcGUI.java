    // save calculation
    public void saveCalculation(String calculation) {
        results.add(calculation); // Save calculation to memory
        System.out.println(calculation); // Print to console
        JOptionPane.showMessageDialog(this, "Calculation saved!");
    }

    // Method to load the saved calculations
    public void loadCalculation() {
        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No calculations found!");
        } else {
            StringBuilder allCalculations = new StringBuilder("Saved Calculations:\n");
            for (String calc : results) {
                allCalculations.append(calc).append("\n");
            }
            JOptionPane.showMessageDialog(this, allCalculations.toString());
        }
    }

         
